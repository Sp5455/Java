package corejava1;

public class MultiDimensionalArray {

	public static void main(String args[]){  

		//declaration and initialization For MultiDimensionalArray
		int a[][]= {{5,7,9},{2,8,6},{1,6,4}}; 

		//Printing Array
	for(int i=0;i<3;i++) {
		
		for (int j=0;j<3;j++) 
			
			System.out.print(a[i][j]+ " ");  
		
		System.out.println( );  
		}
	}
	}
